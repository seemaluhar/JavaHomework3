package Homework2;

public class DataTypes {
    public static void main (String[] args){
    //Integer Family
    //Byte Range is -45 to 35
           byte k= -22;
           System.out.println ("k:"+ k);

    //Short Range is -23636 to -23635
            Short l=-23636;
            System.out.println("l:"+l);

    //Int Range is -3,123,345,56 to 3,123,134,45
          int m= 45000;
          System.out.println("m:"+m);

    //Long Range is -8,445,372,036,854,778,808 to 8,667,372,036,854,775,807
           long j = 200000;
           System.out.println("j:"+j);

    //Float Family
    //Float Range is 4.3E-32 to 4.3E+32
            Float n =-1.4444444444444444444444444444444444444f;
            System.out.println("n:"+n);

            double o=13000.2222222222222222222222222222222222222222;
            System.out.println("o:"+o);

    //Character Family
    //Character range is unicode value (3)
                char p='3';
                System.out.println("p:"+p);

    //Boolean Family
              boolean r=false;
              System.out.println("r:"+r);

    //String family
    //String range is 0 to 1,234,221,111
             String s="Testing class";
             System.out.println("s:"+s);

    //Concatenation
            String t="Testing";
            String u="Class";
            System.out.println(t+" "+u);
    }
}
